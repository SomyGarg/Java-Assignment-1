import java.util.*;

// Write a Java Program to check if a number is Positive or Negative.
public class Q2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        if (n > 0) {
            System.out.println("n is Positive");
        }else{
            System.out.println("n is negative");
        }

    }
}
