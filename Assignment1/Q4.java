// Write a Java program to swap two numbers.


public class Q4 {

    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        // Swap
        int temp = a;
        a = b;
        b = temp;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }
}
